#include "Digit.h"

//////////////////////////////////////////////////////////////////////////////

Digit::Digit(int digit) :m_digit(digit)
{

}

//////////////////////////////////////////////////////////////////////////////

Digit& Digit::operator++() //prefix increment
{
	++m_digit;
	return *this;
}

//////////////////////////////////////////////////////////////////////////////

Digit& Digit::operator++(int) //postfix increment 
{
	m_digit++;
	return *this;
}

//////////////////////////////////////////////////////////////////////////////

std::ostream& operator<<(std::ostream& out, const Digit& digit)
{
	out << "Digit is " << digit.m_digit;
	return out;
}

