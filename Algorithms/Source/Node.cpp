#include "Node.h"

////////////////////////////////////////////////////////////////

BST_Node::BST_Node(int element) : m_left(NULL), m_right(NULL) , m_element(element)
{

}

////////////////////////////////////////////////////////////////

int BST_Node::getElement() const
{
	return m_element;
}

////////////////////////////////////////////////////////////////

BST_Node* BST_Node::getLeftNode() const
{
	return m_left;
}

////////////////////////////////////////////////////////////////

BST_Node* BST_Node::getRightNode() const
{
	return m_right;
}

////////////////////////////////////////////////////////////////

void BST_Node::setLeft(BST_Node* node)
{
	m_right = node;
}

////////////////////////////////////////////////////////////////

void BST_Node::setRight(BST_Node* node)
{
	m_left = node;
}

