#include "SinglyLL-Node.h"

////////////////////////////////////////////////////////////

Node::Node() :m_key(0), m_element(""), m_next(NULL)
{

}

////////////////////////////////////////////////////////////

Node::Node(const char* element) :m_key(0), m_element(element), m_next(NULL)
{
}


////////////////////////////////////////////////////////////

Node::Node(int adjacentNode) :m_key(0), m_element(""), m_next(NULL), m_adjacentVertex(adjacentNode)
{

}

////////////////////////////////////////////////////////////

Node::Node(const std::string& element) :m_key(0), m_element(element), m_next(NULL)
{

}

////////////////////////////////////////////////////////////

void Node::setNext(Node* node)
{
	m_next = node;
}

////////////////////////////////////////////////////////////

const char* Node::getElement() const
{
	return &m_element[0];
}

////////////////////////////////////////////////////////////

unsigned Node::getKey() const
{
	return m_key;
}

////////////////////////////////////////////////////////////

Node* Node::getNext() const
{
	return m_next;
}

////////////////////////////////////////////////////////////

unsigned Node::getVertex() const
{
	return m_adjacentVertex;
}

////////////////////////////////////////////////////////////

void  Node::setKey(unsigned key)
{
	m_key = key;
}

